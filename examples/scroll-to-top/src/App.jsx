function easeInOutExpo(x) {
  return x === 0
    ? 0
    : x === 1
    ? 1
    : x < 0.5
    ? Math.pow(2, 20 * x - 10) / 2
    : (2 - Math.pow(2, -20 * x + 10)) / 2;
}

function scrollToTop(duration = 2000) {
  const el = document.scrollingElement;
  const initialScroll = el.scrollTop;
  let start = null;

  function step(timestamp) {
    if (!start) {
      start = timestamp;
    }

    // Tid i millisekunder som har gått sen
    // scrollToTop() kallades på
    const elapsed = timestamp - start;

    // Hur långt vi kommit i animationen
    // representerat som ett nummer mellan 0 och 1
    const progress = Math.min(1, elapsed / duration);

    // Räknar ut nästa position som ska scrollas till
    const newScrollTop =
      initialScroll - easeInOutExpo(progress) * initialScroll;

    // Sätter det nya scrollTop-värdet
    el.scrollTop = newScrollTop;

    // Om elapsed (antalet ms sedan knappen trycktes på)
    // är mindre än duration (animations-tiden)
    // fortsätt kalla på step
    if (elapsed < duration) {
      window.requestAnimationFrame(step);
    }
  }

  window.requestAnimationFrame(step);
}

